//< is less than. if the number to the left of < is less than the number to the right, it is true. otherwise it is false.

function testLessThan(val) {
  if (val < 25) {  // Change this line
    return "Under 25";
  }

  if (val < 55) {  // Change this line
    return "Under 55";
  }

  return "55 or Over";
}

